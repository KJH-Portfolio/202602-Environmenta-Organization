AFTER INSERT ON MEMBER
FOR EACH ROW
BEGIN
    INSERT INTO ECO_TREE (MEMBER_ID, TREE_LEVEL, SYNCED_EXP)
    VALUES (:NEW.MEMBER_ID, 1, 0);
END;

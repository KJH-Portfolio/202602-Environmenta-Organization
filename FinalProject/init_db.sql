-- [QUIZ] 환경 퀴즈 문항
CREATE TABLE QUIZ (
    QUIZ_NO          NUMBER NOT NULL,
    DIFFICULTY       VARCHAR2(20 BYTE) NOT NULL, -- Easy, Normal, Hard
    POINT            NUMBER DEFAULT 0,          -- 보상 포인트
    QUIZ_QUESTION    VARCHAR2(1000 BYTE) NOT NULL,
    OPTION1          VARCHAR2(500 BYTE) NOT NULL,
    OPTION2          VARCHAR2(500 BYTE) NOT NULL,
    OPTION3          VARCHAR2(500 BYTE) NOT NULL,
    OPTION4          VARCHAR2(500 BYTE) NOT NULL,
    QUIZ_ANSWER      NUMBER NOT NULL,           -- 1~4 중 정답 번호
    QUIZ_EXPLANATION VARCHAR2(2000 BYTE),        -- 정답 해설
    CREATED_AT       DATE DEFAULT SYSDATE,
    CONSTRAINT PK_QUIZ PRIMARY KEY (QUIZ_NO)
);

-- [QUIZ_HISTORY] 퀴즈 참여 결과 내역
CREATE TABLE QUIZ_HISTORY (
    HISTORY_ID NUMBER PRIMARY KEY,
    MEMBER_ID  NUMBER NOT NULL,
    DIFFICULTY VARCHAR2(20) NOT NULL,
    SCORE      NUMBER DEFAULT 0,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_QH_MEMBER_QUIZ FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE
);

-- [QUEST] 환경 보호 퀘스트 목록
CREATE TABLE QUEST (
    QUEST_NO    NUMBER NOT NULL,
    QUEST_TITLE VARCHAR2(300 BYTE) NOT NULL,
    POINT       NUMBER DEFAULT 0,
    CATEGORY    VARCHAR2(50 BYTE),            -- 에너지 절약, 재활용 등
    CREATED_AT  DATE DEFAULT SYSDATE,
    CONSTRAINT PK_QUEST PRIMARY KEY (QUEST_NO)
);

-- [QUEST_HISTORY] 퀘스트 수행 및 인증 내역
CREATE TABLE QUEST_HISTORY (
    HISTORY_ID  NUMBER NOT NULL,
    MEMBER_ID   NUMBER NOT NULL,
    QUEST_NO    NUMBER NOT NULL,
    IMAGE_URL   VARCHAR2(500 BYTE),           -- 인증 사진 경로
    STATUS      VARCHAR2(1 BYTE) DEFAULT 'N', -- N:대기, Y:승인, R:거절
    CREATED_AT  DATE DEFAULT SYSDATE,
    CONSTRAINT PK_QUEST_HISTORY PRIMARY KEY (HISTORY_ID),
    CONSTRAINT FK_QH_MEMBER_QUEST FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
    CONSTRAINT FK_QH_QUEST_ID FOREIGN KEY (QUEST_NO) REFERENCES QUEST(QUEST_NO) ON DELETE CASCADE
);

-- [ATTENDANCE] 출석 정적 (연속 출석 체크용)
CREATE TABLE ATTENDANCE (
    ATTENDANCE_ID    NUMBER PRIMARY KEY,
    USER_ID          NUMBER NOT NULL,
    ATTENDANCE_DATE  DATE DEFAULT SYSDATE NOT NULL,
    CONSECUTIVE_DAYS NUMBER DEFAULT 1 NOT NULL,    -- 연속 출석 일수
    POINTS_EARNED    NUMBER DEFAULT 0 NOT NULL,    -- 보상 받은 포인트
    CONSTRAINT FK_ATTENDANCE_MEMBER FOREIGN KEY (USER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE
);

-- [ECO_TREE] 나무 성장 시스템
CREATE TABLE ECO_TREE (
    MEMBER_ID           NUMBER NOT NULL,
    TREE_LEVEL          NUMBER DEFAULT 1 NOT NULL, -- 1:새싹, 2:어린나무, 3:큰나무, 4:열매
    SYNCED_EXP          NUMBER DEFAULT 0 NOT NULL, -- 성장에 반영된 누적 포인트(경험치)
    LAST_GROWTH_DATE    DATE DEFAULT SYSDATE,
    CONSTRAINT PK_ECO_TREE PRIMARY KEY (MEMBER_ID),
    CONSTRAINT FK_ECO_TREE_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE
);

